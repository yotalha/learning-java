public class Loops {
    public static void forLoop() {
        for(int i = 1; i < 10; i++){
            System.out.println("running in for loop");
        }
    }

    public static void whileLoop() {
        int a = 1;
        while(a < 10) {
            System.out.println("running in while loop");
            a++;
        }
    }
}
